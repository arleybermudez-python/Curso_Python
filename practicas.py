# ingresando es texto de forma vulgar, pero con el int lo convierte en integer que significa
# entero.
# Los numeros flotantes o decimales por norma en Python se escriben con punto y no con coma
decimal = float(input("Escribe un numero decimal:"))
print(decimal)
# Cadenas de texto, si o si siempre van en comillas, se puede guardar cualquier letra,
